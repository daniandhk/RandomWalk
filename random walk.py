print("#kanan 2x kiri, bawah 2x atas, atas 2x kiri")
print("Peluang ke kanan = 2/9")
print("Peluang ke kiri  = 1/9")
print("Peluang ke atas  = 2/9")
print("Peluang ke bawah = 4/9")
